#!/usr/bin/env node
// Generates src/embedded-env.ts with build-time environment variables.

const fs = require("fs");
const path = require("path");

const KEYS = ["SAP_HOST", "SAP_USER", "SAP_PASSWD", "SAP_CLIENT"];

const entries = KEYS
  .filter((k) => process.env[k] !== undefined)
  .map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(process.env[k])},`)
  .join("\n");

const content = `// AUTO-GENERATED by scripts/generate-embedded-env.js — do not edit.
export const embeddedEnv: Record<string, string | undefined> = {
${entries}
};
`;

const outPath = path.join(__dirname, "..", "src", "embedded-env.ts");
fs.writeFileSync(outPath, content, "utf-8");
console.error(
  `Wrote ${KEYS.filter((k) => process.env[k]).length} embedded env vars to ${outPath}`
);
